func buildDaysMap(yearlyDays ...[]base.Date) map[base.Date]struct{} {
	daysMap := make(map[base.Date]struct{})
	for _, days := range yearlyDays {
		for _, day := range days {
			daysMap[day] = struct{}{}
		}
	}

	return daysMap
}
